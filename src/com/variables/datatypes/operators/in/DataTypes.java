package com.variables.datatypes.operators.in;

public class DataTypes {

	public static void main(String[] args) {
		/*Datatypes : It define the 'type' and 'size' of the data
		 * Types:
		 *      1.Primitive data type[Stores 'single' data]
		 *      2.Non-Primitive data type[stores 'multiple' data]
		 * 1.Primitive data type[Stores 'single' data]:
		 * 			Predefined and fixed data
		 *   Ex: byte,short,int,long,float,double,char
		 * 2.Non-primitive data type[stores 'multiple' data]:
		 * 			User defined and dynamic data
		 * 	 Ex: 
		 *     Class[String,String buffer,String builder,Wrapper class,Array,List,Set,Map]	         
		 */
		
		
		//Primitive datatypes:
		byte b=127; 
		short s = 32767;
		int i=979766559;
		long l=677889886567L;  //[L or l =>literal]
		float f=9889.677889898F;//[f or F=>literal]
		double d=8987.9898976686869897;
		char c='q';
		boolean bo=true;
		
		//Non-primitive datatype
		String sn="Hello";
		
		
		
		
		
		
		
	}

}
